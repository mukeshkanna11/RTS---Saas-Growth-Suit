class InvoiceTemplate {
  num(v) {
    const n = Number(v);
    return Number.isFinite(n) ? n : 0;
  }

  money(v) {
    return this.num(v).toFixed(2);
  }

  formatDate(date) {
    if (!date) return "-";
    return new Date(date).toLocaleDateString("en-IN");
  }

  build(data = {}) {
    const {
      company = {},
      customer = {},
      items = [],
      totals = {},

      invoiceId = `INV-${Date.now()}`,

      orderDate,
      purchaseDate,
      paymentDate,
      dueDate,

      paymentStatus = "PENDING",

      notes = "",
      terms = [],

      bank = {}
    } = data;

    return this.toHTML({
      company,
      customer,
      items,
      totals,
      notes,
      terms,
      bank,

      invoice: {
        invoiceId,
        orderDate,
        purchaseDate,
        paymentDate,
        dueDate
      },

      paymentStatus
    });
  }

  toHTML(inv) {
    return `
<!DOCTYPE html>
<html>
<head>
<meta charset="UTF-8">

<title>${inv.invoice.invoiceId}</title>

<style>

body{
  font-family:Arial,sans-serif;
  color:#222;
  padding:40px;
  line-height:1.5;
}

.header{
  display:flex;
  justify-content:space-between;
  margin-bottom:30px;
}

.company{
  width:60%;
}

.invoice-info{
  text-align:right;
}

.status{
  display:inline-block;
  padding:8px 16px;
  border-radius:5px;
  font-weight:bold;
  background:${
    inv.paymentStatus === "PAID"
      ? "#d1fae5"
      : "#fef3c7"
  };
}

.section{
  margin-top:20px;
}

table{
  width:100%;
  border-collapse:collapse;
  margin-top:15px;
}

th{
  background:#f3f4f6;
}

th,td{
  border:1px solid #ddd;
  padding:10px;
}

.summary{
  width:400px;
  margin-left:auto;
  margin-top:25px;
}

.summary td{
  padding:8px;
}

.total{
  font-size:18px;
  font-weight:bold;
}

.footer{
  margin-top:60px;
}

.small{
  color:#666;
  font-size:12px;
}

.signature{
  margin-top:40px;
  text-align:right;
}

</style>
</head>

<body>

<div class="header">

<div class="company">

<h1>${inv.company.name || ""}</h1>

<p>${inv.company.address || ""}</p>

<p>Email: ${inv.company.email || ""}</p>

<p>Phone: ${inv.company.phone || ""}</p>

<p>GSTIN: ${inv.company.gstin || "-"}</p>

<p>PAN: ${inv.company.pan || "-"}</p>

<p>Website: ${inv.company.website || "-"}</p>

</div>

<div class="invoice-info">

<h2>TAX INVOICE</h2>

<p><b>Invoice No:</b> ${inv.invoice.invoiceId}</p>

<p><b>Order Date:</b> ${this.formatDate(inv.invoice.orderDate)}</p>

<p><b>Purchase Date:</b> ${this.formatDate(inv.invoice.purchaseDate)}</p>

<p><b>Payment Date:</b> ${this.formatDate(inv.invoice.paymentDate)}</p>

<p><b>Due Date:</b> ${this.formatDate(inv.invoice.dueDate)}</p>

<div class="status">
${inv.paymentStatus}
</div>

</div>

</div>

<hr>

<div class="section">

<h3>Bill To</h3>

<p>
<b>${inv.customer.name}</b><br>
${inv.customer.email || ""}<br>
${inv.customer.phone || ""}<br>
${inv.customer.address || ""}<br>
GSTIN: ${inv.customer.gstin || "-"}
</p>

</div>

<table>

<thead>
<tr>
<th>#</th>
<th>Description</th>
<th>HSN/SAC</th>
<th>Qty</th>
<th>Price</th>
<th>Total</th>
</tr>
</thead>

<tbody>

${inv.items.map((item,index)=>`
<tr>
<td>${index+1}</td>
<td>${item.name}</td>
<td>${item.hsn || "998314"}</td>
<td>${item.qty}</td>
<td>₹${this.money(item.price)}</td>
<td>₹${this.money(item.qty * item.price)}</td>
</tr>
`).join("")}

</tbody>

</table>

<div class="summary">

<table>

<tr>
<td>Subtotal</td>
<td>₹${this.money(inv.totals.subtotal)}</td>
</tr>

<tr>
<td>Discount</td>
<td>₹${this.money(inv.totals.discountAmount)}</td>
</tr>

<tr>
<td>Taxable Amount</td>
<td>₹${this.money(inv.totals.taxable)}</td>
</tr>

<tr>
<td>CGST</td>
<td>₹${this.money(inv.totals.cgst)}</td>
</tr>

<tr>
<td>SGST</td>
<td>₹${this.money(inv.totals.sgst)}</td>
</tr>

<tr>
<td>IGST</td>
<td>₹${this.money(inv.totals.igst)}</td>
</tr>

<tr class="total">
<td>Grand Total</td>
<td>₹${this.money(inv.totals.total)}</td>
</tr>

</table>

</div>

<div class="section">

<h3>Bank Details</h3>

<p>
Account Name: ${inv.bank.accountName || "-"}<br>
Bank: ${inv.bank.bankName || "-"}<br>
A/C No: ${inv.bank.accountNumber || "-"}<br>
IFSC: ${inv.bank.ifsc || "-"}
</p>

</div>

<div class="section">

<h3>Notes</h3>

<p>${inv.notes || "-"}</p>

</div>

<div class="section">

<h3>Terms & Conditions</h3>

<ul>
${(inv.terms || [])
.map(term => `<li>${term}</li>`)
.join("")}
</ul>

</div>

<div class="signature">

<p>Authorized Signatory</p>

<br><br>

<b>${inv.company.name}</b>

</div>

<div class="footer small">

Generated by ReadyTechSolutions SaaS Billing Engine

</div>

</body>
</html>
`;
  }
}

module.exports = new InvoiceTemplate();